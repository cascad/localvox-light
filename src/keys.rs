//! Физические клавиши (как в client-reliable): раскладка не влияет на хоткеи.

use crossterm::event::KeyCode;

/// Физическая клавиша (не зависит от раскладки). Русская ЙЦУКЕН → Latin QWERTY.
pub fn physical_key(c: char) -> char {
    match c {
        'q' | 'Q' | 'й' | 'Й' => 'q',
        'w' | 'W' | 'ц' | 'Ц' => 'w',
        'e' | 'E' | 'у' | 'У' => 'e',
        'r' | 'R' | 'к' | 'К' => 'r',
        't' | 'T' | 'е' | 'Е' => 't',
        'y' | 'Y' | 'н' | 'Н' => 'y',
        'u' | 'U' | 'г' | 'Г' => 'u',
        'i' | 'I' | 'ш' | 'Ш' => 'i',
        'o' | 'O' | 'щ' | 'Щ' => 'o',
        'p' | 'P' | 'з' | 'З' => 'p',
        'a' | 'A' | 'ф' | 'Ф' => 'a',
        's' | 'S' | 'ы' | 'Ы' => 's',
        'd' | 'D' | 'в' | 'В' => 'd',
        'f' | 'F' | 'а' | 'А' => 'f',
        'g' | 'G' | 'п' | 'П' => 'g',
        'h' | 'H' | 'р' | 'Р' => 'h',
        'j' | 'J' | 'о' | 'О' => 'j',
        'k' | 'K' | 'л' | 'Л' => 'k',
        'l' | 'L' | 'д' | 'Д' => 'l',
        'z' | 'Z' | 'я' | 'Я' => 'z',
        'x' | 'X' | 'ч' | 'Ч' => 'x',
        'c' | 'C' | 'с' | 'С' => 'c',
        'v' | 'V' | 'м' | 'М' => 'v',
        'b' | 'B' | 'и' | 'И' => 'b',
        'n' | 'N' | 'т' | 'Т' => 'n',
        'm' | 'M' | 'ь' | 'Ь' => 'm',
        _ => c.to_ascii_lowercase(),
    }
}

pub fn key_matches(key: KeyCode, expected: char) -> bool {
    match key {
        KeyCode::Char(c) => physical_key(c) == expected,
        _ => false,
    }
}
